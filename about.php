<h3>About HK Housing Analysis</h3>

<p>HK Housing Analysis is a data analysis project that uses housing and economic data to chart out trends between Hong Kong's housing market and its overall economy. This project 
was created by <a href="https://www.linkedin.com/in/angus-leung/">Angus Leung</a>.</p>

<h3>Features Available:</h3>
<ul>
    <li>Display HK Hang Seng Index and 10-year government bond yield versus savings account interest rates.</li>
</ul>
<h3>About the data</h3>

<p><b>Monthly data</b> is shown, as data sources only provide monthly and annual data rather than real-time datasets.</p>

<h3>Hong Kong Housing Market</h3>

<p>Hong Kong is known for being one of the most expensive cities in the world in terms of housing.</p>

<h3>Data Sources:</h3>

<p>HK Housing Analysis aggregates and parses data from these sources below:</p>
<ul>
    <li><a href="https://www.rvd.gov.hk/en/property_market_statistics/index.html">Hong Kong Ratings and Valuation Department (RVD)</a></li>
    <li><a href="https://www.censtatd.gov.hk/hkstat/sub/sp210.jsp">Hong Kong Census and Statistics Department (CenStatd)</a></li>
    <li><a href="https://apidocs.hkma.gov.hk/">Hong Kong Monetary Authority (HKMA)</a></li>
    <li><a href="https://data.gov.hk/en/">Government of Hong Kong</a></li>
</ul>
